    // ── Scroll-spy: highlight the active TOC item as you scroll ──────────────
    (function () {
        const links = Array.from(document.querySelectorAll('.toc-link'));
        const sections = Array.from(document.querySelectorAll('.legal-section'));
        if (!links.length || !sections.length) return;

        function setActive(id) {
            links.forEach(l => l.classList.toggle('is-active', l.dataset.target === id));
        }

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) setActive(entry.target.id);
            });
        }, { rootMargin: '-96px 0px -65% 0px', threshold: 0 });

        sections.forEach(s => observer.observe(s));

        links.forEach(link => {
            link.addEventListener('click', function (e) {
                e.preventDefault();
                document.getElementById(this.dataset.target).scrollIntoView({ behavior: 'smooth' });
            });
        });
    })();
</script>
